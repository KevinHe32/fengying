public class RecipeListModel {
    private String colorNumber;

    private String material;

    private String weishuWeight;

    private String buzhengWeight;

    public String getColorNumber() {
        return colorNumber;
    }

    public void setColorNumber(String colorNumber) {
        this.colorNumber = colorNumber;
    }

    public String getMaterial() {
        return material;
    }

    public void setMaterial(String material) {
        this.material = material;
    }

    public String getWeishuWeight() {
        return weishuWeight;
    }

    public void setWeishuWeight(String weishuWeight) {
        this.weishuWeight = weishuWeight;
    }

    public String getBuzhengWeight() {
        return buzhengWeight;
    }

    public void setBuzhengWeight(String buzhengWeight) {
        this.buzhengWeight = buzhengWeight;
    }
}
